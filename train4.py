import warnings
import os
from ultralytics import YOLO

# =========================================================
# 1. 基础设置
# =========================================================
warnings.filterwarnings('ignore')  # 忽略警告（让输出更干净）

def main():
    # =====================================================
    # 2. 保存路径配置
    # =====================================================
    save_dir = 'runs/detect/sugarcane_train_final'
    os.makedirs(save_dir, exist_ok=True)

    # =====================================================
    # 3. 加载模型（继续训练 or 预训练）
    # =====================================================
    # 推荐两种方式：
    # （1）继续训练（你现在用的）
    model = YOLO(
        r'E:\大学\人工智能\基于yolov8的甘蔗茎节检测\yolov8\runs\detect\sugarcane_train2\weights\best.pt'
    )

    # （2）如果想从头提升上限，可以改成：
    # model = YOLO('yolov8m.pt')

    # =====================================================
    # 4. 训练参数（核心优化部分）
    # =====================================================
    results = model.train(

        # ---------------- 数据 ----------------
        data=r'E:\大学\人工智能\基于yolov8的甘蔗茎节检测\yolov8\datasets\sugarcane stem\data.yaml',

        # ---------------- 训练轮次 ----------------
        epochs=300,              # 比200更稳，避免欠拟合

        # ---------------- 优化器 ----------------
        optimizer='AdamW',       # 小数据更稳定
        lr0=0.0005,              # 初始学习率（降低更稳）
        lrf=0.01,                # 最终学习率（避免学死）
        cos_lr=True,             # 余弦衰减
        weight_decay=0.0005,     # 防过拟合

        # ---------------- 硬件相关 ----------------
        batch=8,                 # 适配8GB显存
        imgsz=640,               # 小目标建议≥640
        device=0,
        workers=8,
        nbs=16,

        # ---------------- 数据增强（小目标关键） ----------------
        mosaic=0.5,              # 不要太高（避免结构破坏）
        mixup=0.1,
        copy_paste=0.2,          # 小目标关键增强

        # 颜色增强（模拟真实环境）
        hsv_h=0.015,
        hsv_s=0.7,
        hsv_v=0.4,

        # 几何增强（适度）
        degrees=10,
        translate=0.1,
        scale=0.5,
        fliplr=0.5,

        # ---------------- 正则化 ----------------
        dropout=0.1,             # 降低（避免影响收敛）
        label_smoothing=0.05,    # 轻量正则

        # ---------------- 训练策略 ----------------
        amp=True,                # 混合精度（提速）
        warmup_epochs=5,
        patience=20,             # 提前停止（更合理）

        # ---------------- 验证 ----------------
        val=True,
        save_json=True,          # 保存评估结果

        # ---------------- 保存 ----------------
        save=True,
        save_period=20,
        project=os.path.dirname(save_dir),
        name=os.path.basename(save_dir),
        exist_ok=True,

        # ---------------- 复现性 ----------------
        seed=42,

        # ---------------- 输出 ----------------
        verbose=True
    )

    print("训练完成！模型已保存至：", save_dir)


# =========================================================
# 5. 主函数入口
# =========================================================
if __name__ == '__main__':
    main()